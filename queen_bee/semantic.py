# ── 概念群组：词 → 同族词 ──
# 核心原则：名词和动词，排除形容词和虚词

_SEMANTIC_GROUPS: dict[str, set[str]] = {}

def _add(main: str, *related: str):
    """添加一个词及其同族词（双向关联）"""
    group = {main} | set(related)
    for w in group:
        if w not in _SEMANTIC_GROUPS:
            _SEMANTIC_GROUPS[w] = set()
        _SEMANTIC_GROUPS[w] |= group

