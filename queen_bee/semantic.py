# ── 核心函数 ──

def expand(query: str) -> set[str]:
    """对一段文本提取核心词并扩展同族概念

    输入: "之前写的计算器程序"
    输出: {"写", "创建", "开发", "程序", "计算器", ...}
    """
    import re
    words = set()
    # 中文分词（简单切分：2-4字词 + 单字）
    for w in re.findall(r"[\u4e00-\u9fff]{2,6}", query):
        words.add(w)
        # 同族扩展
        if w in _SEMANTIC_GROUPS:
            words |= _SEMANTIC_GROUPS[w]
    # 单字补充（不在停用词表中的单字也加入）
    stop = set("的了吗么是和我你他她它们与或不就都也要会个这那有对为所以可以因为如果虽然然而而且然后之其及被把从到着等吧呢啊")
    for ch in query:
        if '\u4e00' <= ch <= '\u9fff' and ch not in stop:
            words.add(ch)
            if ch in _SEMANTIC_GROUPS:
                words |= _SEMANTIC_GROUPS[ch]
    return words


def expand_tags(text: str) -> list[str]:
    """从一段文本提取适合做标签的扩展词"""
    words = expand(text)
    result = sorted(words, key=lambda x: (-len(x), x))
    return result[:20]


